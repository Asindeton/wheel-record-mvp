export const VALIDATION_REGEX = {
  email: /^[\w.-]+@[a-zA-Z_-]+?(?:\.[a-zA-Z]{2,6})+$/,
  telegramUsername: /^@[a-zA-Z0-9]+$/,
  flatNumber: /^(?=.+)(?:[1-9]\d*|0)?(?:\.\d+)?$/,
  phone: /^(\+7|7|8)?[\s-]?\(?[489][0-9]{2}\)?[\s-]?[0-9]{3}[\s-]?[0-9]{2}[\s-]?[0-9]{2}$/,
  url: /^(https?:\/\/)?([\da-z.-]+)\.([a-z.]{2,6})([/\w.-]*)*\/?$/,
};
